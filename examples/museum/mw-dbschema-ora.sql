CREATE TABLE WORK(WNO INTEGER,AUTHOR INTEGER,TITLE VARCHAR2(500),SUPPORT VARCHAR2(500),C_DATE VARCHAR2(500),HEIGHT VARCHAR2(500),WIDTH VARCHAR2(500),ACQUIRED VARCHAR2(500),TEXT VARCHAR2(500),AUDIO VARCHAR2(500),PICTURE VARCHAR2(500),MOVIE VARCHAR2(500));
DROP table ART_CNTY;
