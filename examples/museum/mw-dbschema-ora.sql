DROP table MUSEUM;
CREATE TABLE MUSEUM(MNO INTEGER,NAME VARCHAR2(500),CITY VARCHAR2(500),COUNTRY VARCHAR2(500),URL VARCHAR2(500));
DROP table OWNERSHIP;
